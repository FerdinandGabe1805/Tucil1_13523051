public class Point{
    private int point1;
    private int point2;
  
    public Point(int a, int b){
      this.point1=a;
      this.point2=b;
    }
  
    public int getPoint1() {
        return point1;
    }
  
    public int getPoint2() {
        return point2;
    }
  
    public void setPoint1(int a) {
      this.point1 = a;
    }
  
    public void setPoint2(int b) {
        this.point2 = b;
    }
  
  }
  
